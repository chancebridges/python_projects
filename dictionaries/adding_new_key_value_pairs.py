# dictionaries are dynamic structures, and you can add new key-value pairs to a dictionary at any time
# to add a new key-value, you would give the name of the dictionary followed by the new key in square brackets along with the new value

alien_0 = { 'color' : 'green', 'points' : 5}
print(alien_0)


alien_0['x_position'] = 0
alien_0['y_position'] = 25
print(alien_0)
