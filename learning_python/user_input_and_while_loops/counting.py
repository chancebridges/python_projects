current_number = 1
while current_number <= 5:
    print(current_number)
    current_number += 1

# we start counting at 1 which is designated by line 1
# the while loop is then set to keep running as long as the value of current_number is less
# than or equal to 5. the code inside the loop prints the value of currrent_number 
# and then adds 1 to that value with current_number += 1

# the operator is shorthand for current_number = current_number + 1
